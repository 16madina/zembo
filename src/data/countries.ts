// Helper function to validate phone number length for a country
export const isValidPhoneLength = (phone: string, countryCode: string): boolean => {
  const country = countries.find(c => c.code === countryCode);
  if (!country) return phone.length >= 6;
  
  const lengths = Array.isArray(country.phoneLength) 
    ? country.phoneLength 
    : [country.phoneLength];
  
  return lengths.includes(phone.length);
};

// Helper function to get expected phone length(s) for a country
export const getExpectedPhoneLength = (countryCode: string): string => {
  const country = countries.find(c => c.code === countryCode);
  if (!country) return "6+";
  
  const lengths = Array.isArray(country.phoneLength) 
    ? country.phoneLength 
    : [country.phoneLength];
  
  return lengths.length > 1 
    ? `${Math.min(...lengths)}-${Math.max(...lengths)}` 
    : String(lengths[0]);
};

